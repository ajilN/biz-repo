package com.href.biz.dto;

public class FacilityDTO {
	
	private String id;
	private String category;
	private String rate;
	private String serailNoFrom;
	private String serialNoTo;
	private String grossAmount;
	private String reservationAmount;
	private String onlineAmount;
	
	
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getRate() {
		return rate;
	}
	public void setRate(String rate) {
		this.rate = rate;
	}
	public String getSerailNoFrom() {
		return serailNoFrom;
	}
	public void setSerailNoFrom(String serailNoFrom) {
		this.serailNoFrom = serailNoFrom;
	}
	public String getSerialNoTo() {
		return serialNoTo;
	}
	public void setSerialNoTo(String serialNoTo) {
		this.serialNoTo = serialNoTo;
	}
	public String getGrossAmount() {
		return grossAmount;
	}
	public void setGrossAmount(String grossAmount) {
		this.grossAmount = grossAmount;
	}
	public String getReservationAmount() {
		return reservationAmount;
	}
	public void setReservationAmount(String reservationAmount) {
		this.reservationAmount = reservationAmount;
	}
	public String getOnlineAmount() {
		return onlineAmount;
	}
	public void setOnlineAmount(String onlineAmount) {
		this.onlineAmount = onlineAmount;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
	
	

}
